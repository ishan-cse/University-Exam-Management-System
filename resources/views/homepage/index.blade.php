                            <h4>Earned Credits</h4>
                            <p>The courses in which a student obtains C or a higher grade will be counted as credits earned by him/her. If a student obtains D grade in any core course of any Trimester, he/she will have to repeat the course. In the case of D grade in an optional course, s/he may choose to take a substitute course, if available.</p>
                            <h4>Regular Grading</h4>
                            <p>D Grade will not be counted for Grade Point Average (GPA) calculation but will be shown on the Grade Sheet. When a student repeats a course in which s/he previously obtained D/F grade, s/he will be given just immediate lower grade that s/he obtained in the repeated course. But in case s/he obtains C grade, that will be maintained and this grade will be shown in the transcript. If a student has to repeat a course due to punishment on him/her, the grade obtained will be maintained. If a student obtains a grade other than D in a course, s/he will be allowed to repeat the course for the purpose of grade improvement.</p>
                            <h4>Repetition of Course(s)</h4>
                            <p>If a student repeats a course for which s/he obtained D/F grade (in any previous Trimester), s/he may be allowed to repeat the continuous assessment (i.e. Class Test/Assignment/Term paper) to improve the grade, but s/he has to apply to the Head of the Department through concerned course teacher within 20 working days from the beginning of the Trimester. However, the marks of class attendance and participation will be taken from the previous record.</p>
